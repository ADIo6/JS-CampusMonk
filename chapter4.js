// Switch Case 

let a = 10;
let b = 9;

switch (a + b) {
    case 13:
        console.log("'10 + 3 is 13'");
        break;
    case 22:
        console.log("'10 + 3 is 13'");
        break;
    case 19:
        console.log("'10 + 9 is 19'");
        break;

    default:
        console.log('All the above cases are false');
        
        break;
}